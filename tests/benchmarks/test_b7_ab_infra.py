def test_pairwise_compare_order_consistent_win():
    # A is better in BOTH orders -> A wins, order_consistent, no flip.
    # order1 presents (A=1, B=2): verdict "1" = A better.
    # order2 presents (B=1, A=2): verdict "2" = A better.
    verdicts = iter(["1", "2"])

    def stub_verdict(d1, d2, **kw):
        return next(verdicts)

    out = R.pairwise_compare("DOC A", "DOC B", cascade=[("x", "y")],
                             _verdict=stub_verdict)
    assert out["winner"] == "a"
    assert out["order_consistent"] is True
    assert out["order_flip"] is False


def test_pairwise_compare_flip_is_a_tie():
    # Judge prefers whatever is shown FIRST (position bias) -> inconsistent -> tie.
    # order1 (A=1,B=2): "1" (A). order2 (B=1,A=2): "1" (B). They disagree.
    verdicts = iter(["1", "1"])

    def stub_verdict(d1, d2, **kw):
        return next(verdicts)

    out = R.pairwise_compare("DOC A", "DOC B", cascade=[("x", "y")],
                             _verdict=stub_verdict)
    assert out["winner"] == "tie"
    assert out["order_flip"] is True


def test_pairwise_compare_scrubs_both_docs():
    seen = []

    def stub_verdict(d1, d2, **kw):
        seen.append((d1, d2))
        return "tie"

    R.pairwise_compare("You are bert's writer.\nDOC A claude opus",
                       "DOC B sonnet", cascade=[("x", "y")], _verdict=stub_verdict)
    blob = " ".join(d for pair in seen for d in pair).lower()
    assert "bert" not in blob and "claude" not in blob and "opus" not in blob
    assert "sonnet" not in blob       # both docs blinded before the judge sees them


