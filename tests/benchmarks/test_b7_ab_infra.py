# ── neutral grading reuses core unmodified ───────────────────────────

class _FakeGrade:
    def __init__(self, ws, med):
        self.weighted_score = ws
        self.medians = med
        self.variances = dict.fromkeys(med, 0.0)
        self.overall_variance = 0.0
        self.passes = ws >= 0.7
        self.dropped = []

    def to_dict(self):
        return {"weighted_score": self.weighted_score, "medians": self.medians,
                "variances": self.variances, "overall_variance": 0.0,
                "passes": self.passes, "dropped": []}


def test_neutral_judge_grade_reuses_core_and_collapses_K():
    from core import quality
    contract = quality.QualityContract(5, 4, 4, 4, 3, 4, 3, 3, pass_threshold=0.7)
    calls = []
    dims = ["correctness", "completeness", "provenance", "defensibility",
            "usability", "honesty", "reproducibility", "efficiency"]

    scripted = [0.60, 0.80, 0.70]   # 3 regrades -> median weighted_score = 0.70

    def fake_grade(artifact, gaps, *, contract, system_prompt_fn=None, **kw):
        calls.append({"contract": contract, "spf": system_prompt_fn})
        ws = scripted[len(calls) - 1]
        return _FakeGrade(ws, dict.fromkeys(dims, 3))

    out = R.neutral_judge_grade("art", "gaps", contract, K=3, _grade=fake_grade)
    assert len(calls) == 3                                  # K=3 regrades
    assert all(c["contract"] is contract for c in calls)    # SAME contract reused
    assert all(c["spf"] is R.NEUTRAL_JUDGE_PROMPT_FN for c in calls)  # neutral framing
    assert abs(out["weighted_score"] - 0.70) < 1e-9         # median-of-K
    assert len(out["regrades"]) == 3


