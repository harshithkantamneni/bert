# ── baseline telemetry + guards ──────────────────────────────────────

def _cli_json(**over):
    base = {"usage": {"input_tokens": 1200, "cache_creation_input_tokens": 300,
                      "cache_read_input_tokens": 500, "output_tokens": 800},
            "duration_ms": 42000, "total_cost_usd": 0.37,
            "session_id": "sess1234", "is_error": False}
    base.update(over)
    return json.dumps(base)


def test_baseline_telemetry_extraction(tmp_path):
    art = tmp_path / "artifact.md"
    art.write_text("x" * 400)
    out = R.parse_baseline_result(0, _cli_json(), art)
    assert out["failed_to_produce"] is False
    assert out["tokens_in_gross"] == 1200 + 300 + 500
    assert out["tokens_in_net"] == 1200
    assert out["tokens_out"] == 800
    assert abs(out["latency_secs"] - 42.0) < 1e-9
    assert out["model_calls"] == 1
    assert out["session_id"] == "sess1234"
    assert abs(out["cost_usd_estimate"] - 0.37) < 1e-9


def test_baseline_guards_reject_bad_output(tmp_path):
    good = tmp_path / "a.md"; good.write_text("y" * 200)
    tiny = tmp_path / "b.md"; tiny.write_text("short")          # <100 bytes
    # returncode != 0
    assert R.parse_baseline_result(1, _cli_json(), good)["failed_to_produce"]
    # non-JSON stdout
    assert R.parse_baseline_result(0, "not json", good)["failed_to_produce"]
    # is_error true
    assert R.parse_baseline_result(0, _cli_json(is_error=True), good)["failed_to_produce"]
    # artifact too small
    r = R.parse_baseline_result(0, _cli_json(), tiny)
    assert r["failed_to_produce"]
    # all failures score 0 / not-pass, never silently dropped
    assert r["weighted_score"] == 0.0 and r["passes"] is False


