def test_make_stat_row_excludes_failed_arm():
    a = {"tier": "T1", "instance": "i1", "weighted_score": 0.86,
         "failed_to_produce": False, "tokens_total": 23000, "latency_secs": 90.0}
    b = {**a, "weighted_score": 0.86}
