# ── split + scrub (blinding) ─────────────────────────────────────────

def test_split_on_limitations_deterministic():
    body = "# Title\n\nReal content here.\n"
    for hdr in ("## Limitations", "## Gaps", "## What I could not verify"):
        text = body + f"\n{hdr}\n- a\n- b\n- c\n"
        art, gaps = R.split_on_limitations(text)
        assert art.rstrip() == body.rstrip()
        assert hdr in gaps and "- a" in gaps
    # No limitations header -> (full text, "") and never raises.
    art, gaps = R.split_on_limitations("# Only body\n\nno gaps section")
    assert gaps == "" and art.startswith("# Only body")
    # Multiple headers -> split at the LAST one.
    multi = body + "\n## Gaps\n- early\n\n## Limitations\n- final\n"
    art2, gaps2 = R.split_on_limitations(multi)
    assert "- final" in gaps2


def test_scrub_anonymizes_arms_equally():
    dirty = ("You are bert's researcher.\n# Findings (bert_run_C31)\n"
             "## Cycle 3 summary\nbert concluded X. See bert_run_C31_writer.md.\n")
